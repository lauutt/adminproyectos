package bdd;

public class Proyecto {
	
	private String titulo;
	private Integer id;
	
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	
	public void setID(Integer id) {
		this.id = id;
	}
	
	public String getTitulo() {
		return this.titulo;
	}
	
	public Integer getID() {
		return this.id;
	}
	

}
